    private void jbImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbImprimirActionPerformed
        if ((jtfIdPesada.getText().equals("") || jtfIdPesada.getText().equals(" "))
                || (jtfPlacas.getText().equals("") || jtfPlacas.getText().equals(" "))
                || (jtfOperador.getText().equals("") || jtfOperador.getText().equals(" "))
                || (jtfHoraEntrada.getText().equals("") || jtfHoraEntrada.getText().equals(" "))
                || (jtfPesoEntrada.getText().equals("") || jtfPesoEntrada.getText().equals(" "))
                || (jtfHoraSalida.getText().equals("") || jtfHoraSalida.getText().equals(" "))
                || (jtfPesoSalida.getText().equals("") || jtfPesoSalida.getText().equals(" "))) {
            new Error(this, rootPaneCheckingEnabled, "Datos incompletos");
        } else {
            Pesada pesada = new Pesada();
            pesada.setIdUsuario(new UsuarioDAO().getIdUsuario(jcbUsuarios.getItemAt(jcbUsuarios.getSelectedIndex())));
            pesada.setiDPesada(Integer.parseInt(jtfIdPesada.getText()));
            pesada.setPlacas(jtfPlacas.getText());
            pesada.setOperador(jtfOperador.getText());
            pesada.setEntrada(jtfHoraEntrada.getText());
            pesada.setPesoEntrada(jtfPesoEntrada.getText());
            pesada.setSalida(jtfHoraSalida.getText());
            pesada.setPesoSalida(jtfPesoSalida.getText());
            Imprimir i = new Imprimir(this, rootPaneCheckingEnabled, pesada);
            i.setVisible(true);
            System.out.println("Imprimiendo comprobante");
            jbGuardarActionPerformed(evt);
        }
    }//GEN-LAST:event_jbImprimirActionPerformed

