    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        Pesada pesada = new Pesada();
        boolean error = false;
        char tipo;
        int id = 0;
        try {
            id = Integer.parseInt(jtfIdPesada.getText());
        } catch (Exception e) {
            new Error(this, rootPaneCheckingEnabled, "Debe agregar un nuevo ID Pesada");
            error = true;
        }
        if (error == false) {
            if (!jrbCliente.isSelected() && !jrbProveedor.isSelected()) {
                new Error(this, rootPaneCheckingEnabled, "Debe indicar si es CLIENTE o PROVEEDOR");

            } else {
                pesada.setIdUsuario(new UsuarioDAO().getIdUsuario(jcbUsuarios.getItemAt(jcbUsuarios.getSelectedIndex())));
                pesada.setiDPesada(id);
                pesada.setPlacas(jtfPlacas.getText());
                pesada.setOperador(jtfOperador.getText());
                pesada.setEntrada(jtfHoraEntrada.getText());
                pesada.setPesoEntrada(jtfPesoEntrada.getText());
                pesada.setSalida(jtfHoraSalida.getText());
                pesada.setPesoSalida(jtfPesoSalida.getText());
                if (jrbCliente.isSelected()) {
                    tipo = 'C';
                } else {
                    tipo = 'P';
                }
                if (jrbProveedor.isSelected()) {
                    tipo = 'P';
                } else {
                    tipo = 'C';
                }
                pesada.setTipoPesada(tipo);
                PesadaDAO pdao = new PesadaDAO();

                if (pdao.modificar(pesada)) {
                    jtfIdPesada.setText(null);
                    jtfPlacas.setText(null);
                    jtfOperador.setText(null);
                    jtfHoraEntrada.setText(null);
                    jtfPesoEntrada.setText(null);
                    jtfHoraSalida.setText(null);
                    jtfPesoSalida.setText(null);

                    Guardado g = new Guardado(this, rootPaneCheckingEnabled);
                } else {
                    System.out.println("problema al guardar el registro");
                }
            }
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

